var m = parseInt(prompt("Введите номер масти m"))
switch (m) {
    case 1:
        alert("пики")
        break
    case 2:
        alert("трефы")
        break
    case 3:
        alert("бубны")
        break
    case 4:
        alert("червы")
        break
    default:
        alert("Введите номер от 1 до 4")
}